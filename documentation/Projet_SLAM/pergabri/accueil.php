<div id="Description"><h2>Présentation:</h2>
    <p>Le groupe Pergabri conçoit et fabrique en France une gamme de systèmes de protection pour la piscine. </p>
    <p>Pergabri propose également différents types de pergola.</p>
    <p> Design, innovation, fiabilité, grande qualité, sécurité sont les marques de fabrique de l’entreprise…</p>
    <p>Le groupe Pergabri met également son expertise au service d’applications industrielles et professionnelles spécifiques : 
        piscines publiques, bacs à sel, stations de lavage, stations de traitement de l’eau, terrasses de restaurants, abris sur bateau…</p>

</div>
<div id="Produits">
    <h2>Unité de production:</h2>
    <p>Pergabri est une entreprise à taille humaine qui conçoit et fabrique ses produits au sein de deux unités de production équipées d’outils de très haute précision et de dernière génération.
        Tous les abris de piscine sont fabriqués à Cazères (près de Toulouse), quant aux pergolas, elles sont fabriquées en Vendée et dans la Drôme.
        Chaque étape de fabrication est supervisée et contrôlée par nos techniciens dans le moindre détail … du cousu main.</p>
